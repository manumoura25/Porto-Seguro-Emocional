// HTML BASE


function montarHTML(projeto) {
    return `
        <h2 class="titulo_projeto">${projeto.titulo}</h2>

        <div class="container-slide">
            <div class="carrossel-slide" id="slider">
                ${projeto.carrossel.map(src => `
                    <img class="img-slides" src="${src}" alt="Imagem do projeto">
                    <img class="img-slides" src="${src}" alt="Imagem do projeto">
                `).join("")}
            </div>

        </div>

        ${projeto.textos.map(txt => `
            <p class="justified">${txt}</p>
        `).join("")}

        <div class="container_imgs">
            ${projeto.imagensProcesso.map(src => `
                <img class="img_processo_projeto" src="${src}" alt="Imagem do processo">
            `).join("")}
        </div>

        <span id="line"></span>

        <div class="container_img">
            ${projeto.imagensFinal.map(src => `
                <img class="img_projeto" src="${src}" alt="Resultado final">
            `).join("")}
        </div>

        <div class="descricao">
            ${projeto.descricao.map(desc => `
                <p class="justified">${desc}</p>
            `).join("")}
        </div>
    `;
}

// INSERÇÃO DO PROJETO NA PÁGINA

function carregarProjeto(index) {
    const container = document.getElementById("container");
    const projeto = projetos[index];

    container.innerHTML = montarHTML(projeto);
}


